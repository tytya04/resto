const { Scenes } = require('telegraf');
const { User } = require('../database/models');
const logger = require('../utils/logger');

const addAdminScene = new Scenes.BaseScene('add_admin');

// –í—Ö–æ–¥ –≤ —Å—Ü–µ–Ω—É
addAdminScene.enter(async (ctx) => {
  await ctx.reply(
    'üëë <b>–î–æ–±–∞–≤–ª–µ–Ω–∏–µ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞</b>\n\n' +
    '–í–≤–µ–¥–∏—Ç–µ Telegram ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, –∫–æ—Ç–æ—Ä–æ–≥–æ —Ö–æ—Ç–∏—Ç–µ –Ω–∞–∑–Ω–∞—á–∏—Ç—å –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–º.\n\n' +
    '–î–ª—è –æ—Ç–º–µ–Ω—ã –≤–≤–µ–¥–∏—Ç–µ /cancel',
    { parse_mode: 'HTML' }
  );
});

// –û–±—Ä–∞–±–æ—Ç–∫–∞ –≤–≤–æ–¥–∞ ID
addAdminScene.on('text', async (ctx) => {
  const text = ctx.message.text;
  
  if (text === '/cancel') {
    await ctx.reply('‚ùå –û–ø–µ—Ä–∞—Ü–∏—è –æ—Ç–º–µ–Ω–µ–Ω–∞');
    return ctx.scene.leave();
  }
  
  // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –≤–≤–µ–¥–µ–Ω –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π ID
  const telegramId = parseInt(text);
  if (isNaN(telegramId)) {
    return ctx.reply('‚ö†Ô∏è –í–≤–µ–¥–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π Telegram ID (—á–∏—Å–ª–æ)');
  }
  
  try {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
    let user = await User.findOne({ where: { telegram_id: telegramId } });
    
    if (user) {
      // –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –ø—Ä–æ–≤–µ—Ä—è–µ–º –µ–≥–æ —Ä–æ–ª—å
      if (user.role === 'admin') {
        await ctx.reply('‚ö†Ô∏è –≠—Ç–æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É–∂–µ —è–≤–ª—è–µ—Ç—Å—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–º');
        return ctx.scene.leave();
      }
      
      // –û–±–Ω–æ–≤–ª—è–µ–º —Ä–æ–ª—å
      const oldRole = user.role;
      user.role = 'admin';
      await user.save();
      
      await ctx.reply(
        '‚úÖ <b>–ê–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä –¥–æ–±–∞–≤–ª–µ–Ω —É—Å–ø–µ—à–Ω–æ!</b>\n\n' +
        `–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: ${user.first_name || ''} ${user.last_name || ''}\n` +
        `Username: @${user.username || '–Ω–µ—Ç'}\n` +
        `–ü—Ä–µ–¥—ã–¥—É—â–∞—è —Ä–æ–ª—å: ${oldRole}\n` +
        `–ù–æ–≤–∞—è —Ä–æ–ª—å: admin`,
        { parse_mode: 'HTML' }
      );
      
      // –£–≤–µ–¥–æ–º–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –æ –Ω–æ–≤–æ–π —Ä–æ–ª–∏
      try {
        await ctx.telegram.sendMessage(
          telegramId,
          'üéâ <b>–ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º!</b>\n\n' +
          '–í–∞–º –ø—Ä–µ–¥–æ—Å—Ç–∞–≤–ª–µ–Ω—ã –ø—Ä–∞–≤–∞ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ –≤ —Å–∏—Å—Ç–µ–º–µ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –∑–∞–∫—É–ø–∫–∞–º–∏.\n\n' +
          '–¢–µ–ø–µ—Ä—å –≤–∞–º –¥–æ—Å—Ç—É–ø–Ω–∞ –∫–æ–º–∞–Ω–¥–∞ /admin_panel –¥–ª—è —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è —Å–∏—Å—Ç–µ–º–æ–π.',
          { parse_mode: 'HTML' }
        );
      } catch (e) {
        logger.error('Error notifying new admin:', e);
      }
    } else {
      // –°–æ–∑–¥–∞–µ–º –Ω–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è-–∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
      await ctx.reply(
        '‚ö†Ô∏è –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å —Ç–∞–∫–∏–º ID –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ —Å–∏—Å—Ç–µ–º–µ.\n\n' +
        '–•–æ—Ç–∏—Ç–µ —Å–æ–∑–¥–∞—Ç—å –Ω–æ–≤–æ–≥–æ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞?\n' +
        '–í–≤–µ–¥–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –≤ —Ñ–æ—Ä–º–∞—Ç–µ:\n' +
        '–ò–º—è –§–∞–º–∏–ª–∏—è username\n\n' +
        '–ù–∞–ø—Ä–∏–º–µ—Ä: –ò–≤–∞–Ω –ò–≤–∞–Ω–æ–≤ ivanov\n\n' +
        '–î–ª—è –æ—Ç–º–µ–Ω—ã –≤–≤–µ–¥–∏—Ç–µ /cancel'
      );
      
      ctx.scene.state.telegramId = telegramId;
      ctx.scene.state.waitingForUserData = true;
      return;
    }
  } catch (error) {
    logger.error('Error in addAdminScene:', error);
    await ctx.reply('‚ùå –ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –¥–æ–±–∞–≤–ª–µ–Ω–∏–∏ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞');
  }
  
  return ctx.scene.leave();
});

// –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è –Ω–æ–≤–æ–≥–æ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
addAdminScene.on('text', async (ctx) => {
  if (!ctx.scene.state.waitingForUserData) return;
  
  const text = ctx.message.text;
  
  if (text === '/cancel') {
    await ctx.reply('‚ùå –û–ø–µ—Ä–∞—Ü–∏—è –æ—Ç–º–µ–Ω–µ–Ω–∞');
    return ctx.scene.leave();
  }
  
  const parts = text.split(' ');
  if (parts.length < 2) {
    return ctx.reply('‚ö†Ô∏è –í–≤–µ–¥–∏—Ç–µ –∫–∞–∫ –º–∏–Ω–∏–º—É–º –∏–º—è –∏ —Ñ–∞–º–∏–ª–∏—é');
  }
  
  const firstName = parts[0];
  const lastName = parts[1];
  const username = parts[2] || null;
  
  try {
    const user = await User.create({
      telegram_id: ctx.scene.state.telegramId,
      first_name: firstName,
      last_name: lastName,
      username: username,
      role: 'admin',
      is_active: true
    });
    
    await ctx.reply(
      '‚úÖ <b>–ù–æ–≤—ã–π –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä —Å–æ–∑–¥–∞–Ω —É—Å–ø–µ—à–Ω–æ!</b>\n\n' +
      `ID: ${user.telegram_id}\n` +
      `–ò–º—è: ${user.first_name} ${user.last_name}\n` +
      `Username: @${user.username || '–Ω–µ—Ç'}\n` +
      `–†–æ–ª—å: admin`,
      { parse_mode: 'HTML' }
    );
    
    // –£–≤–µ–¥–æ–º–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    try {
      await ctx.telegram.sendMessage(
        user.telegram_id,
        'üéâ <b>–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å!</b>\n\n' +
        '–í—ã –±—ã–ª–∏ –¥–æ–±–∞–≤–ª–µ–Ω—ã –≤ —Å–∏—Å—Ç–µ–º—É —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –∑–∞–∫—É–ø–∫–∞–º–∏ –∫–∞–∫ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä.\n\n' +
        '–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—É /start –¥–ª—è –Ω–∞—á–∞–ª–∞ —Ä–∞–±–æ—Ç—ã –∏ /admin_panel –¥–ª—è –¥–æ—Å—Ç—É–ø–∞ –∫ –ø–∞–Ω–µ–ª–∏ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞.',
        { parse_mode: 'HTML' }
      );
    } catch (e) {
      logger.error('Error notifying new admin:', e);
    }
  } catch (error) {
    logger.error('Error creating admin:', error);
    await ctx.reply('‚ùå –ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞');
  }
  
  return ctx.scene.leave();
});

module.exports = addAdminScene;